<?php

class Calculatrice {    

    public function addition($nb1, $nb2) {
        return $nb1 + $nb2;
    }

    public function soustraction($nb1, $nb2) {
        return $nb1 - $nb2;
    }

    public function multi($nb1, $nb2) {
        return $nb1 * $nb2;
    }

    public function divi($nb1, $nb2) {
        return $nb1 / $nb2;
    }
}